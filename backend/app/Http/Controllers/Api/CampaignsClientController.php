     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        try {
            $client = auth('apiclient')->user();
            $campaigns = new Campaigns;
            $campaigns = $campaigns->select('id', 'name')->where('id_client', $client->id)->where('active', 1);
            $campaigns = $campaigns->get();
            return response()->json(LibraryController::responseApi($campaigns));
        } catch (Exception $e) {
            if ($e->getCode()) {
                $code = $e->getCode();
            }else {
                $code = 500;
            }
            return response()->json(LibraryController::responseApi([],$e->getMessage(), $code));
        }
    }

